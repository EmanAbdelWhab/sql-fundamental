--join using clause to join based on selected column
SELECT
EMPLOYEES.EMPLOYEE_ID,
EMPLOYEES.FIRST_NAME,
DEPARTMENT_ID,
DEPARTMENTS.DEPARTMENT_NAME
FROM EMPLOYEES join
DEPARTMENTS
USING (department_id)
ORDER by EMPLOYEE_ID;

--join on
SELECT
EMPLOYEES.EMPLOYEE_ID,
EMPLOYEES.FIRST_NAME,
DEPARTMENTS.DEPARTMENT_ID,
DEPARTMENTS.DEPARTMENT_NAME
FROM EMPLOYEES JOIN DEPARTMENTS 
ON (EMPLOYEES.DEPARTMENT_ID=DEPARTMENTS.DEPARTMENT_ID)
ORDER by EMPLOYEE_ID;

SELECT emp.EMPLOYEE_ID, emp.FIRST_NAME, emp.SALARY, grades.job_level
FROM EMPLOYEES emp JOIN
JOB_GRADES grades
on emp.SALARY between GRADES.LOWEST_SAL And GRADES.HIGHEST_SAL;

SELECT worker.EMPLOYEE_ID,
worker.FIRST_NAME,
worker.manager_id,
manager.FIRST_NAME
FROM EMPLOYEES worker JOIN EMPLOYEES MANAGER
on(worker.MANAGER_ID=manager.EMPLOYEE_ID);

SELECT 
emp.EMPLOYEE_ID,
emp.FIRST_NAME,
emp.DEPARTMENT_ID,
dept.DEPARTMENT_NAME,
dept.LOCATION_ID,
loc.city
from EMPLOYEES emp
join DEPARTMENTS DEPT
on(emp.DEPARTMENT_ID = DEPT.DEPARTMENT_ID) 
join LOCATION LOC
on (dept.LOCATION_ID=LOC.LOCATION_ID)
order by EMP.EMPLOYEE_ID;


--left outer join
SELECT
EMPLOYEES.EMPLOYEE_ID,
EMPLOYEES.FIRST_NAME,
DEPARTMENTS.DEPARTMENT_ID,
DEPARTMENTS.DEPARTMENT_NAME
FROM EMPLOYEES
left outer JOIN DEPARTMENTS
on(EMPLOYEES.EMPLOYEE_ID=DEPARTMENTS.DEPARTMENT_ID)
order by EMPLOYEE_ID;

--right outer join
SELECT
EMPLOYEES.EMPLOYEE_ID,
EMPLOYEES.FIRST_NAME,
DEPARTMENTS.DEPARTMENT_ID,
DEPARTMENTS.DEPARTMENT_NAME
FROM EMPLOYEES
RIGHT outer JOIN DEPARTMENTS
on(EMPLOYEES.EMPLOYEE_ID=DEPARTMENTS.DEPARTMENT_ID)
order by EMPLOYEE_ID;

--full outer join
SELECT
EMPLOYEES.EMPLOYEE_ID,
EMPLOYEES.FIRST_NAME,
DEPARTMENTS.DEPARTMENT_ID,
DEPARTMENTS.DEPARTMENT_NAME
FROM EMPLOYEES
FULL outer JOIN DEPARTMENTS
on(EMPLOYEES.EMPLOYEE_ID=DEPARTMENTS.DEPARTMENT_ID)
order by EMPLOYEE_ID;
